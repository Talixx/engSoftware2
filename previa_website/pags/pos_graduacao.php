		<h2>Apresentação Strictu Sensu</h2>
		<br>

		A pós-graduação stricto sensu compreende os cursos de mestrado e doutorado acadêmicos e os cursos de mestrado profissional (Portaria Capes 80 de 1998). Chama-se Programa de Pós-Graduação ao conjunto de cursos de uma determinada área do conhecimento, consistindo usualmente do par mestrado-doutorado. Contudo, um programa pode também consistir de apenas um mestrado ou de mais de um curso de mestrado e/ou doutorado, desde que na mesma área. A UFF tem, em dezembro de 2009, 50 programas de pós-graduação aprovados pela CAPES; 31 destes programas consistem de mestrado e doutorado, 5 correspondem a mestrados profissionais e os 14 restantes, a mestrados acadêmicos. <p><a href="http://uffs.edu.br" style="background-color:#ffffa0">Clique aqui</a></p> para saber a relação dos programas, telefones, endereços e conceitos CAPES.

		Somente possuem validade nacional os diplomas emitidos por instituições de ensino superior brasileiras, que provenham de programas de pós-graduação stricto sensu avaliados pela Capes, com nota igual ou superior a 3. Essa avaliação se dá a cada três anos, e leva em conta indicadores tais como a abrangência, áreas de concentração, metodologias de ensino, infra-estrutura utilizada, corpo docente e produção científica do programa, dentre outros. A oferta de cursos fora da sede, ou em quaisquer modalidades não previstas no projeto inicial avaliado e reconhecido pela Capes deve, necessariamente, ser submetida a nova avaliação. Para saber como proceder para criar um curso de pós-graduação na UFF, <p><a href="http://uffs.edu.br" style="background-color:#ffffa0">clique aqui.</a></p>
		Texto: http://www.proppi.uff.br/posgraduacao/apresentacao-stricto-sensu.


