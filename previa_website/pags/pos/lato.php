<h2>Apresentação Lato Sensu</h2>
<br><br>

A pós-graduação lato sensu - cursos de especialização / MBA / Residência - caracteriza-se pela formação de pessoal em nível de especialização profissional, visando à capacitação e ao aprimoramento do educando em áreas específicas do saber. É um programa de estudo de curta duração (mínimo de 360 horas) e com flexibilidade curricular em termos de conteúdo, disciplinas e atividades acadêmicas. Para a conclusão do curso, exige-se apresentação de trabalho final individual ou monografia. É aberta a candidatos diplomados em cursos superiores e que atendam às exigências das instituições de ensino - art. 44, III, Lei 9.394/1996. <p><a href="http://uffs.edu.br" style="background-color:#ffffa0">clique aqui.</a></p>
