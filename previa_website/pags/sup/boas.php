		<p>Este guia inicial é uma leitura obrigatória para o bom uso do produto.</p><p>
		Primeira dica, verifique a configuração do cache (<a style="color:#EF7F1A" href="index.php?pag=suporte&m=08" target="_blank">como fazer</a>), o recomendado para usuários com internet estável é 
		manter sempre em <b>0 dias</b>, esta alteração garante que os arquivos estejam devidamente sincronizados com o servidor do 
		Amplidrive, ativar o cache é recomendado apenas para usuários que ficam dias sem acesso a internet e desejam trabalhar offline (<a target="_blank" href="index.php?pag=suporte&m=07" style="color:#EF7F1A">ler mais</a>).
		</p>
		<p>Segunda dica, apesar do AmpliDrive criar uma unidade de rede na máquina do usuário, temos que ter um certo <b>cuidado 
		simples ao salvar arquivos para evitar conflitos/duplicações.</b>  </p>
		<p>Após terminar a alteração no arquivo desejado e clicar no botão salvar, uma pequena janela abrirá no canto inferior
		direito da tela, como a figura abaixo:</p><p></p>
		<img src="images/sup/boa1.png">
		<p></p>
		<p>É preciso esperar esperar a sincronização do arquivo recentemente salvo com o servidor do AmpliDrive, normalmente com arquivos
		comuns do Microsoft Office (Excel, Word) por exemplo, a sincronização demora em torno de 10 segundos, nesto momento o usuário <b>não deve clicar
		no botão salvar novamente,</b> até o arquivo sincronizar com o servidor.</p>

		<p>A imagem abaixo mostra a mensagem avisando que o arquivo desejado foi sincronizado com sucesso, agora já é possível apertar no botão salvar 
		novamente.</p>
