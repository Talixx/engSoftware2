<h1>Acessar pastas e arquivos</h1>
	<div class="col-md-12">
	<p>Ao usar o AmpliDrive em um PC o programa emula uma nova unidade de rede, como o usuário está familiarizado
com sua rede de servidor de arquivos da empresa normal.
A letra da unidade padrão é "U:" mas pode ser qualquer letra de unidade disponível a partir da letra "J", o usuário pode
definir a letra da unidade na página de configurações.</p>

<p>Nesta unidade criada pelo AmpliDrive, o usuário pode acessar os arquivos a partir de qualquer aplicativo(Ex: Word, Excel, Adobe Reader) normalmente.</p>
