<h1>Compartilhando pastas/arquivos</h1>
	<div class="col-md-12">
	<p>Para compartilhar arquivos e pastas com AmpliDrive,<br>
	Vá para o lista de pastas e clique no ícone "Compartilhar" à esquerda do nome do compartilhamento.</p>
	<img src="images/sup/10-comp.png">
	<p></p><p></p>
	<p>Se o usuário não aparecer na lista, comece a digitar o nome no campo "Pesquisar / Adicionar usuário".</p>
	<img src="images/sup/10-wcom.png" style="width:90%;">
	<p></p>
	<p>Em seguida, conectar-se a "Novo usuário" será exibido, clique no link e preencha o 
	nosso pop-up com informações sobre o usuário que você deseja compartilhar.</p>
	<img src="images/sup/10-wwc.png" style="width:90%;">
	<p></p>
	<p>Se você compartilha com um usuário externo que não está usando o sistema já, o usuário 
	tem que ativar sua conta antes de usar o compartilhamento.</p>

	<p>Se você é um usuário externo, você não tem permissão para compartilhar os dados.</p>
