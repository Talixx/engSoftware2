<h1>Histórico, versões e deletados</h1>
	<div class="col-md-12">
	<p>Histórico, versões e recuperar arquivos deletados é possível a partir da aplicação web AmpliDrive,
	se alguma vez você já tentou apagar o arquivo errado, não se preocupe com AmpliDrive você pode facilmente
	recuperar arquivos apagados.</p>
	<p></p>
	<p>Vá até a pasta aonde o arquivo deletado estava, habilite a opção "Ficheiros eliminado:s"</p>
	<img src="images/sup/09-del.png" style="width:90%;">
	<p></p>
	<p>Perceba que o "Documento 4" está com um ícone de lixeira que o identifica como um arquivo
	deletado.</p>
	<p>Basta passar o mouse em cima do ícone da lixeira que opção de ver as versões e recuperar o 
	arquivo aparecem</p>
	<img src="images/sup/09-rec.png">
	<p>O primeiro ícone a direita da lixeira serve para ver as versões que esse arquivo possui, 
	já o ícone mais a direita serve para recuperar o arquivo(também pode recuperar uma pasta inteira) 
	novamente a pasta.</p>
	<p></p><p></p>
	<p>Lembrando que se o arquivo não foi deletado, passar o mouse em cima do ícone dele abre as seguintes 
	opções:</p>
	<img src="images/sup/09-hist.png">
	<p>São as seguintes opções: (enumeradas da esquerda para a direita)</p>
	<p>1 - Criar um link para poder compartilhar o arquivo com outras pessoas que ainda não usam o AmpliDrive.<br>
	   2 - Verificar o histórico do arquivo.<br>
	   3 - Deletar o arquivo.<br>
	   4 - Renomear o arquivo.</p>
