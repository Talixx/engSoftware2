<h1>O que fazer quando há conflito de versões</h1>
	<div class="col-md-12">
		<p>O conflito ocorre quando um usuário que está offline edita e salva um documento ao
		mesmo tempo que um usuário online, portanto haverá um conflito quando este usuário que
		está offline conectar-se a internet, duas versões diferentes serão criadas.</p>
		<p>O usuário receberá uma mensagem avisando o conflito assim que conectar-se a internet.
Todo os arquivos são listados na área de arquivos em conflito:</p>
		<img src="images/sup/04-pri.jpg"><p></p>
		<img src="images/sup/04-seg.jpg"><p></p>

		<p>Para eliminar o conflito, o usuário deve proceder da seguinte forma:
			<p>Clique duplo em cima do arquivo na lista de arquivos em conflito, isso abrirá para o
			usuário a pasta aonde estão os arquivos conflitantes.</p>
			<p>Desta forma o usuário deve abrir a duas versões e decidir qual versão s erá mantida e
			qual será excluída, após isso o conflito irá desaparecer.</p></p>
			<img src="images/sup/04-ter.png"><p></p>
			<p>Atenção: Mesmo se o usuário não conseguiu visualizar a mensagem de arquivos conflitantes, é possível entrar na pasta aonde
			está o arquivo em conflito e achá-lo facilmente pelo nome, que foi alterado como na imagem acima. </p>
	</div>
</div>