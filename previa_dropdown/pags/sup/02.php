<h1>Arquivos online/offine</h1>
	<div class="col-md-12">
		<p>Por padrão o AmpliDrive lista todos os arquivos e pastas que o usuário tem acesso conforme foi concedido nas configurações de pastas da empresa.
		Porém estes arquivos e pastas podem ocupar muito espaço. Portanto o usuário vai baixar para o seu computador só o que é de 
		seu interesse, esses arquivos são mantidos em um cache para acesso e edição rápida.</p>

		<p>Desta forma, o usuário tem acesso aos arquivos, mas sem ocupar espaço no dispositivo local.
		Para indicar ao usuário que o arquivo não está ocupando espaço em disco o arquivo está identificado com um ícone de sobreposição (X cinza).
		Isso também indica que o arquivo só é acessível quando o usuário tem acesso à Internet.</p>

		<p>Se o usuário abrir um arquivo com um X, seja de dentro de um programa, ou diretamente a partir do gerenciador de arquivos será
		baixado e o X cinza será removido indicando que o usuário agora tem uma cópia local do arquivo no computador.
		</p>

		<p>Qualquer alteção no arquivo, o arquivo será automaticamente atualizado no servidor de arquivos corporativo.</p>
